// toastUtils.js
import { toast } from "react-toastify";

export const notify = {
  success: (message) => {
    toast.success(message || "✅ Success!", {
      position: "top-right",
      autoClose: 3000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      theme: "colored",
    });
  },
  error: (message) => {
    toast.error(message || "❌ Something went wrong!", {
      position: "top-right",
      autoClose: 4000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      theme: "colored",
    });
  },
};







